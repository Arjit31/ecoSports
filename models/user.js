const mongodb = require('mongoose');

mongoose.connect('mongodb://localhost:27017/EcoSport')

const userSchema = mongoose.userSchema({
    name: {
        type: String,
        required: true
    },
    username: {
        type: String,
        required: true,
        unique: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    }
})

module.exports = mongoose.model('user', userSchema);